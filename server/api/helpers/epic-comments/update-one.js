module.exports = {
  inputs: {
    record: {
      type: 'ref',
      required: true,
    },
    values: {
      type: 'json',
      required: true,
    },
    project: {
      type: 'ref',
      required: true,
    },
    board: {
      type: 'ref',
      required: true,
    },
    epic: {
      type: 'ref',
      required: true,
    },
    actorUser: {
      type: 'ref',
      required: true,
    },
    request: {
      type: 'ref',
    },
  },

  async fn(inputs) {
    const { values } = inputs;

    const epicComment = await EpicComment.qm.updateOne(inputs.record.id, values);

    if (epicComment) {
      sails.sockets.broadcast(
        `board:${inputs.board.id}`,
        'epicCommentUpdate',
        {
          item: epicComment,
        },
        inputs.request,
      );
    }

    return epicComment;
  },
};
